export default function EmojiPicker({ onSelect }: EmojiPickerProps) {
  const [searchQuery, setSearchQuery] = useState('');
  const [activeTab, setActiveTab] = useState('smileys');

  const filteredEmojis = searchQuery
    ? Object.values(emojiCategories)
        .flatMap(cat => cat.emojis)
        .filter(emoji => emoji.includes(searchQuery))
    : emojiCategories[activeTab as keyof typeof emojiCategories]?.emojis || [];

  return (
    <div className="w-full max-w-sm bg-background border rounded-lg shadow-lg">
      <div className="p-3 border-b">
        <div className="relative">
          <Search className="absolute left-3 top-1/2 -translate-y-1/2 h-4 w-4 text-muted-foreground" />
          <Input
            placeholder="Search emojis..."
            value={searchQuery}
            onChange={(e) => setSearchQuery(e.target.value)}
            className="pl-9 h-9"
          />
        </div>
      </div>

      {!searchQuery && (
        <Tabs value={activeTab} onValueChange={setActiveTab} className="w-full">
          <TabsList className="w-full grid grid-cols-6 h-12">
            {Object.entries(emojiCategories).map(([key, { label }]) => (
              <TabsTrigger key={key} value={key} className="text-lg">
                {label}
              </TabsTrigger>
            ))}
          </TabsList>
        </Tabs>
      )}

      <ScrollArea className="h-64">
        <div className="grid grid-cols-8 gap-1 p-2">
          {filteredEmojis.map((emoji, index) => (
            <Button
              key={`${emoji}-${index}`}
              variant="ghost"
              className="h-10 w-10 p-0 text-xl hover:bg-accent touch-manipulation"
              onClick={() => onSelect(emoji)}
            >
              {emoji}
            </Button>
          ))}
        </div>
        {filteredEmojis.length === 0 && (
          <div className="flex items-center justify-center h-32 text-sm text-muted-foreground">
            No emojis found
          </div>
        )}
      </ScrollArea>
    </div>
  );
}
